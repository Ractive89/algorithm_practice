int main(void)
{
	int n,i;
	__int64 fib[1000005];
	fib[0]=fib[1]=1;
	for(i=2;i<1000005;i++){
		fib[i]=(fib[i-1]+fib[i-2])%10007;
	}
	scanf("%d",&n);
	printf("%I64d\n",fib[n-1]);		
	
	return 0;
}
